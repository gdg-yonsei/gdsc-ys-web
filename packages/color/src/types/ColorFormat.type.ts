/**
 * ### Color Format Enumeration
 *
 * Enum of supported color formats
 */
export enum ColorFormat {
  /**
   * ### RGB
   *
   * RGB type
   */
  RGB = '#RGB',

  /**
   * ### RGBA
   *
   * RGBA type
   */
  RGBA = '#RGBA',

  /**
   * ### RRGGBB
   *
   * RRGGBB type
   */
  RRGGBB = '#RRGGBB',

  /**
   * ### RRGGBBAA
   *
   * RRGGBBAA type
   */
  RRGGBBAA = '#RRGGBBAA',

  /**
   * ### rgb(r, g, b) type
   *
   * rgb(r, g, b) type
   */
  rgb = 'rgb(r, g, b)',

  /**
   * ### rgb(r, g, b, a) type
   *
   * rgb(r, g, b, a) type
   */
  rgba = 'rgba(r, g, b, a)',
}
